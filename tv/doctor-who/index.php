                <li><a href="http://www.bbc.co.uk/blogs/doctorwho/entries/e03e1e3f-c5c3-4d48-8212-6bf6d1ad9608">Filming Begins on Doctor Who, Series 9</a> (bbc.co.uk, 14 Jan 2015) - Confirms Toby Whithouse two-parter, and therefore the writer and director for Block 1.</li>
                <li><a href="http://www.doctorwhotv.co.uk/moffat-confirms-his-next-two-stories-for-series-9-71450.htm">Moffat Confirms his Next Two Stories for Series 9</a> (Doctor Who TV, 30 Jan 2015) and <a href="http://www.doctorwhotv.co.uk/blink-director-for-series-9-71282.htm">Blink Director for Series 9</a> (Doctor Who TV, 12 Feb 2015) - The second article confirms the director for Block 2. The first article quotes a section where Moffat appears to suggest that two of his scripts are in the second production block, but this quote was missing from the Radio Times article. If this part is correct, the assumption is that Hettie MacDonald is directing two standalone stories by Moffat. I've also assumed they are episodes 1 and 2, which may not be the case.</li>
                <li><a href="http://www.doctorwhotv.co.uk/missy-back-in-two-part-series-9-opener-70966.htm">Missy Back in Two-Part Series 9 Opener</a> (Doctor Who TV, 19 Feb 2015) - My previous assumption was mostly correct. Block 2 has episodes 1 and 2, written by Moffat and directed by Hettie MacDonald. However, they also form a two-part story. This means for the first time in the new series of Doctor Who, we have back to back two-parters spanning the first four episodes of the series. Considering we mostly likely have a two-part finale as well (as the penultimate episode is a cliffhanger), are we on track for an entire series of two-parters?</li>
                <li><a href="http://www.doctorwhotv.co.uk/series-9-episode-5-filming-soon-72280.htm">Series 9: Episode 5 Filming Soon</a> (Doctor Who TV, 19 March 2015) - For now, all this confirms is that episode 5 is a single parter.</li>
                <li><a href="http://www.doctorwhotv.co.uk/series-9-the-girl-who-died-and-the-woman-who-lived-72698.htm">Series 9: The Girl Who Died and The Woman Who Lived</a> (Doctor Who TV, 30 March 2015) - Moffat previously said "<em>The rule I’ve got is that you won’t be absolutely certain whether a show is going to be a two-parter or not. And sometimes something that looks like it’s going to be a single, isn’t a single.</em>" (DWM #484) On this basis, I've made some assumptions. Previously we heard that episode 5 is a single-parter; however, the two new titles appear to be linked. So, I've assumed these episodes could be numbered 5 and 6 in the run, with a linked storyline, but not an official two-parter. I've also assumed that we haven't missed any episodes and that the stories are not being filmed out of order. I could be totally wrong, or I could be dead right. It's speculation, after all.</li>
                <li><a href="http://www.bbc.co.uk/blogs/doctorwho/entries/38990615-a32e-41b5-9765-137ecd727f86">Game of Thrones Star to Guest in Doctor Who!</a> (BBC Latest News - Doctor Who, 30 March 2015) - This confirms Ed Bazalgette as the director for the aforementioned episodes 5 and 6.</li>
                <li><a href="http://www.radiotimes.com/news/2015-04-03/could-doctor-who-series-nine-be-entirely-two-parters">Could Doctor Who series nine be entirely two-parters?</a> (Radio Times, 3 April 2015) - This is where things get interesting. Apparently, the first six episodes will be three consecutive two-parters. I speculated on this a few weeks ago, when the opener was revealed to be a two-parter.</li>
                <li>Episode duration - <a href="http://www.theagency.co.uk/clients/clientdisplay.html?viewListing=Nzg1">Daniel O'Hara's client page</a> at The Agency suggests the episodes he directed run for 60 minutes each.</li>
