                <li><a href="http://www.doctorwhotv.co.uk/merlin-atlantis-director-for-series-9-73897.htm">Merlin &amp; Atlantis Director for Series 9</a> (Doctor Who TV, 26 May 2015) - Confirms another director + 2 x 60 minute episodes. I've assumed these to be episodes 9 and 10, and also to be a two-parter. If this is correct, it is looking like the entire series could be comprised of two-part episodes, with each episode running to 60 minutes.</li>
                <li><a href="http://www.doctorwhotv.co.uk/series-9-rigsy-returns-in-episode-10-73957.htm">Rigsy returns in episode 10</a> (Doctor Who TV, 10 June 2015) - Confirms writer and director for episode 10. The director matches up with the Merlin &amp; Atlantis director point above, however, it doesn't say anything about episode 10 being a two-parter. For now, I've taken Wikipedia's lead and put episode 9 as a Mark Gatiss story. Episodes 5 and 6 have one director but different writers; that could be the case for episodes 9 and 10, too.</li>
