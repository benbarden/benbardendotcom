            <h2>Original sleeve notes:</h2>
            <h3>From undeveloped tunes to monthly tracks</h3>
            <p>
                Since writing my first song in 1995, music composition has been an interesting experience, if a little unpredictable.
                At first, I made up melodies and scribbled them onto manuscript paper.
                When I got my first PC, I put some of the old material into music notation software, and wrote even more new material.
                Then I discovered how to improve the sound quality of my compositions, and started sharing some of the better tracks via my website.
                But I’ve always been better at starting new tracks than finishing off the old ones.
            </p>
            <p>
                In December 2004, I decided to get a bit more organised.
                The plan was to complete one track a month, every month, throughout 2005.
                It didn’t matter whether they were old tracks or brand new tracks.
                Despite the occasional missed deadline, I’ve managed to put out a track for every month from January
                right through to December.
                I’d say it’s worked well. It will continue in 2006.
            </p>
            <h3>Why I compose</h3>
            <p>
                If a picture’s worth a thousand words, then music can be worth even more.
                I use music to tell stories, share feelings and express emotions.
            </p>
            <p>
                As an example, consider this. From time to time, I have very vivid and colourful dreams.
                When I wake up, there are so many things I want to say, but I can’t say them fast enough.
                Some of the tracks on this album were inspired by dreams of faraway places.
                Music allows me to communicate an enormous amount of information in a very compressed amount of time.
                You probably won’t conjure up exactly the same thoughts as I did, but that’s part of the enjoyment – Interpretation.
                For every story I tell, you’ll picture it slightly differently.
            </p>
            <p>
                Apparently, there’s a book in everyone, but not everyone has the inclination or the discipline to write it.
                This is my book.
            </p>
            <h3>My first album</h3>
            <p>
                “A World Beyond” is the result of countless late nights and a lot of dedication,
                scattered over a period of 10 years.
                Hopefully, my second album will take less than 10 years to create!
            </p>
