            <h2>Original sleeve notes:</h2>
            <h3>From monthly tracks to total freedom</h3>
            <p>
                Writing a track a month was a great way to get me back into writing music.
                However, in early 2006, I started coming up with so many new ideas that monthly deadlines were not
                frequent enough. In March, I concluded the monthly tracks concept.
                Within a week, I’d completed three more tracks, penned one demo, and started several brand new pieces.
            </p>
            <h3>Drawing comparisons to days gone by</h3>
            <p>
                The last time I wrote this much material was between 1996 and 1999, when I was just starting out.
                This was before the “newness” of composing started to wear off.
                Seven years later, things have picked up again for two reasons.
                Firstly, people are starting to notice my music.
                Secondly, I know I can improve on “A World Beyond”.
                This realisation was helped with the introduction of some new software.
            </p>
            <h3>Listening to your feedback</h3>
            <p>
                “A World Beyond” has a number of problems.
                There’s too much piano, unrealistic instruments, not enough bass, a general lack of variety,
                and many tracks sound empty and unfinished.
                These interpretations are a combination of the feedback I received, and my own evaluation of the album.
                With “Besidewalk Plight”, I have attempted to address all of these issues.
            </p>
            <h3>A range of styles</h3>
            <p>
                All of “Besidewalk Plight” falls within the category of instrumental music.
                However, it’s quite a different sound to the mostly piano-based compositions from my first album.
                Many of the arrangements are much more complex, with sounds ranging from orchestral to electronic.
                Some of it is ambient, but the album is too diverse to be classified as a single style.
            </p>
