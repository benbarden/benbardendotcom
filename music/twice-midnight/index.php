            <h2>Chronology</h2>
            <p>
                Despite being a full-length album of original tracks, Twice Midnight is difficult to place in my album
                chronology.
            </p>
            <p>
                I started working on Twice Midnight after completing Besidewalk Plight,
                intending this to be my third album. However, the album didn't progress too well.
            </p>
            <p>
                I shelved Twice Midnight and set to work on Hidden in the Beyond. I also started The Unravelling of
                Travelling. I ended up with three albums in progress at the same time.
            </p>
            <p>
                In the end, I finished and released The Unravelling of Travelling just two months before I released
                Twice Midnight. As most of Twice Midnight was written before I started working on The Unravelling of
                Travelling, Twice Midnight felt like a bit of a backwards step.
            </p>
            <p>
                So, I now consider Twice Midnight to be my third album proper, even if it wasn't released in that order.
            </p>
            <h2>Track links</h2>
            <p>
                Several of the tracks are linked in some way:
            </p>
            <ul>
                <li>Midnight theme - this features in Midnight 1, Twilight Tide, Prosperity, and Midnight 2.
                It also features in "Take Me Downstairs" from The Unravelling of Travelling - that track was originally
                written for Twice Midnight. As a result, it creates a link between the two albums.</li>
                <li>Waiting for the Sun - the riff pattern features at the start of Opposites Attract, as a
                way to link the tracks together.</li>
                <li>Moon Eclipse - towards the end of the album, Solar Eclipse appears as a remix of Moon Eclipse.</li>
                <li>Woodland Clearing - Between the Trees was written as an immediate follow-up to Woodland Clearing,
                with an intro intended to continue and advance the feel of Woodland Clearing. Unfortunately the
                track itself just wasn't that good.</li>
                <li>Circumference - the original version of this track is included as a bonus track. A newer remix,
                Radial Circumference, appears in the main album track listing.</li>
                <li>Gravity (Ben's Low Gravity Remix) - despite being a bonus track, this is a remix of Gravity from
                Besidewalk Plight - thereby linking those two albums together, too.</li>
                <li>Twice Midnight - the aforementioned Midnight theme links a few tracks together. However, there's
                a more obvious link between two tracks and also the album title - Midnight 1 is a short intro, and
                Midnight 2 develops its ideas into a much longer track.</li>
            </ul>
